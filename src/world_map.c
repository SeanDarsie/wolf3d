void makeworld3(t_graphic *info)
{
  uint32_t y;
  uint32_t x;

  y = 0;
  while (y <= MAP_H)
    {
      x = 0;
      while (x <= MAP_W)
	{
	  if (x >= 10 && x <= 17 && y >= 10 && y <= 17)
	    info->map[y][x] = 3;
	  x++;
	}
      y++;
    }
}

void makeworld(t_graphic *info)
{
  uint32_t y;
  uint32_t x;

  y = 0;
  //ft_bzero(info->map, MAP_W * MAP_H * 4);
  while (y <= MAP_H)
    {
      x = 0;
      while (x <= MAP_W)
	{
	  if (y == 0 || y == MAP_H || x == 0 || x == MAP_W)
	    info->map[y][x] = 1;
	  else
	    info->map[y][x] = 0;
	  x++;
	}
      y++;
    } 
}
