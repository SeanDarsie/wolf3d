#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = (-n);
	}
	if (n == -2147483648)
	{
		ft_putchar_fd('2', fd);
		n -= 2000000000;
	}
	if (n / 10 >= 1)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd('0' + (n % 10), fd);
}
