#include "libft.h"

int			ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (s1 && s2)
	{
		while (s1 && s2 && i < n)
		{
			if (s1[i] != s2[i])
				return (0);
			i++;
		}
		if (s1[n - 1] == s2[n - 1])
			return (1);
	}
	return (0);
}
