#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = (-n);
	}
	if (n == -2147483648)
	{
		ft_putchar('2');
		n -= 2000000000;
	}
	if (n / 10 >= 1)
		ft_putnbr(n / 10);
	ft_putchar('0' + (n % 10));
}
